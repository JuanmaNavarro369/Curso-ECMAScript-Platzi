

function square(num1, num2){
    return num1 * num2
};
square(5,4);

const square = (num1, num2) => {
    return num1 * num2           //ARROW FUNCTION
};
square(5,4);

const square = num => num * num; /* ALTERNATIVA ARROW FUNCTION */
square(5);

